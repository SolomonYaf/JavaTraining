package JavaLoops;

public class ForLoop {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i+=2) {
            System.out.println(i);

            int n = 5;
            // for loop
            for (int j = 1; j <= n; ++j) {
                System.out.println("Java is fun");
            }
        }
    }
}