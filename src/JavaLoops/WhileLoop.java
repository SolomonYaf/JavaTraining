package JavaLoops;

public class WhileLoop {
    public static void main(String[] args) {
        int i=1;

        while (i<=10){
            System.out.println(i);
            i+=2;
            int  j=1;
            while (j<=5){
                System.out.println("Java is fun");
                j++;
            }
        }
    }
}
